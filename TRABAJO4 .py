# Generar una lista con los primeros 20 números pares
numeros_pares = [i * 2 for i in range(1, 21)]

# Calcular la suma de todos los elementos de la lista
suma_total = sum(numeros_pares)

# Mostrar la lista y la suma total
print("Lista de los primeros 20 números pares:", numeros_pares)
print("Suma total de los elementos de la lista:", suma_total)